    private void btnAsignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsignarActionPerformed
        // TODO add your handling code here:
        String id = null;
        int selec = tablaGuias.getSelectedRow();
        if (selec == -1) return;
        ModeloTablaGuias mt= (ModeloTablaGuias)tablaGuias.getModel();
        id = (String) mt.getValueAt(selec, 0);
        
        fa.asignarGuia(id, a);
        VActividades va = (VActividades) super.getParent();
        va.actualizarTabla(va.getFilaGuia());
        this.dispose();
    }//GEN-LAST:event_btnAsignarActionPerformed

     public final void actualizarTabla() {
        String nombre = txtNombre.getText();
        List<Empleado> empleados =  fa.buscarEmpleado(nombre, a);
        ModeloTablaGuias mt= (ModeloTablaGuias)tablaGuias.getModel();
        mt.setFilas(empleados);
        mt.fireTableDataChanged();
        if (mt.getRowCount() > 0)
            tablaGuias.setRowSelectionInterval(0, 0);
    }
     
